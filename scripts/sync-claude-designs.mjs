#!/usr/bin/env node
import { readFileSync, writeFileSync, readdirSync, mkdirSync, existsSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const labsDir = resolve(here, '..', 'data', 'labs');
const outDir = resolve(here, '..', 'claude-designs');
const outFile = resolve(outDir, 'design-tokens.jsx');

if (!existsSync(outDir)) {
  mkdirSync(outDir, { recursive: true });
}

const labs = {};
for (const file of readdirSync(labsDir)) {
  if (!file.endsWith('.json')) continue;
  const id = file.replace(/\.json$/, '');
  const content = JSON.parse(readFileSync(resolve(labsDir, file), 'utf8'));
  labs[id] = content;
}

const banner = `// AUTO-GENERATED by scripts/sync-claude-designs.mjs from data/labs/*.json
// Do not edit by hand. Edit the JSON files and re-run \`npm run sync:designs\`.
// Re-upload this file to your Claude Designs project after each sync.
`;

const body = `export const DESIGN_TOKENS = ${JSON.stringify(labs, null, 2)};

export function getLab(id) {
  return DESIGN_TOKENS[id] || DESIGN_TOKENS.core;
}

export function tokenToCssVars(lab) {
  if (!lab) return {};
  return {
    '--accent': lab.accent,
    '--accent-rgb': lab.accentRgb,
    '--accent-name': lab.accentName,
    '--secondary-accent': lab.secondaryAccent,
    '--secondary-accent-rgb': lab.secondaryAccentRgb,
    '--font-mono': lab.fonts && lab.fonts.mono,
    '--font-display': lab.fonts && lab.fonts.display,
    '--week-1': lab.weekAccents && lab.weekAccents.W1,
    '--week-2': lab.weekAccents && lab.weekAccents.W2,
    '--week-3': lab.weekAccents && lab.weekAccents.W3,
    '--week-4': lab.weekAccents && lab.weekAccents.W4,
  };
}

export default DESIGN_TOKENS;
`;

writeFileSync(outFile, banner + body, 'utf8');
console.log(`[sync:designs] wrote ${outFile} with labs: ${Object.keys(labs).join(', ')}`);
